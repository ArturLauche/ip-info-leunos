export const SUPPORTED_LANGUAGES = ["de", "en", "fr", "es", "it", "pt", "nl", "pl"] as const;

export type SupportedLanguage = (typeof SUPPORTED_LANGUAGES)[number];

export function isSupportedLanguage(value: string | null | undefined): value is SupportedLanguage {
  return !!value && SUPPORTED_LANGUAGES.includes(value as SupportedLanguage);
}

export function resolveLanguage(value: string | null | undefined): SupportedLanguage {
  return isSupportedLanguage(value) ? value : "de";
}

